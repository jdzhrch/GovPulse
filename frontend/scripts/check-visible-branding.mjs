import assert from 'node:assert/strict'
import { readFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = dirname(fileURLToPath(import.meta.url))
const frontendDir = resolve(scriptDir, '..')

const read = (relativePath) => readFileSync(resolve(frontendDir, relativePath), 'utf8')

const brandConfig = read('src/brand.ts')
assert.match(brandConfig, /export const BRAND_NAME = 'Policy Dog'/)
assert.match(brandConfig, /export const SHARE_IMAGE_PREFIX = 'policy-dog'/)

const indexHtml = read('index.html')
assert.match(indexHtml, /<title>Policy Dog - Regulatory Compliance Platform<\/title>/)

const notFoundHtml = read('public/404.html')
assert.match(notFoundHtml, /<title>Policy Dog<\/title>/)

const layout = read('src/components/Layout.tsx')
assert.doesNotMatch(layout, /GovPulse/)
assert.match(layout, /BRAND_NAME/)
assert.match(layout, /BRAND_TAGLINE/)

const helpGuide = read('src/components/HelpGuide.tsx')
assert.doesNotMatch(helpGuide, /GovPulse/)
assert.match(helpGuide, /HELP_GUIDE_DESCRIPTION/)
assert.match(helpGuide, /HELP_GUIDE_SUMMARY/)

const shareCard = read('src/components/ShareCard.tsx')
assert.doesNotMatch(shareCard, /Generated by GovPulse/)
assert.match(shareCard, /BRAND_MONOGRAM/)
assert.match(shareCard, /BRAND_NAME/)
assert.match(shareCard, /SHARE_CARD_QR_HINT/)

const gapAnalysis = read('src/pages/GapAnalysis.tsx')
assert.doesNotMatch(gapAnalysis, /GovPulse-/)
assert.match(gapAnalysis, /buildShareImageFilename/)

const main = read('src/main.tsx')
assert.match(main, /document\.title = BROWSER_TITLE/)

console.log('Visible branding matches Policy Dog.')
